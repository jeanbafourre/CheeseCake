
public class CheeseCake extends Gateau {

	String fromageBlanc; 
	String cremeFraiche;
	String biscuits;
	String gelatine;
	String saveur;
	public String getFromageBlanc() {
		return fromageBlanc;
	}
	public void setFromageBlanc(String fromageBlanc) {
		this.fromageBlanc = fromageBlanc;
	}
	public String getCremeFraiche() {
		return cremeFraiche;
	}
	public void setCremeFraiche(String cremeFraiche) {
		this.cremeFraiche = cremeFraiche;
	}
	public String getBiscuits() {
		return biscuits;
	}
	public void setBiscuits(String biscuits) {
		this.biscuits = biscuits;
	}
	public String getGelatine() {
		return gelatine;
	}
	public void setGelatine(String gelatine) {
		this.gelatine = gelatine;
	}
	public String getSaveur() {
		return saveur;
	}
	public void setSaveur(String saveur) {
		this.saveur = saveur;
	}
	
	
	
}
